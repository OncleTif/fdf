#include "fdf.h"

void	ft_draw(void *mlx, void *win)
{
	int	x;
	int	y;

	x = 200;
	while (x < 300)
	{
		y = 200;
		while (y < 300)
		{
			mlx_pixel_put(mlx, win, x, y, 0x00FFFF00);
			y++;
		}
		x++;
	}
}
